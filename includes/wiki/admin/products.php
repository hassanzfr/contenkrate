    <h3>Adding a New Product</h3>
    <ol>
        <li>Go to <strong>Products</strong> in the admin menu</li>
        <li>Click <strong>Add Product</strong></li>
        <li>Fill in all required fields</li>
        <li>Upload product images</li>
        <li>Set pricing and inventory</li>
        <li>Click <strong>Save Product</strong></li>
    </ol>
    
    <h3>Editing Products</h3>
    <p>To modify an existing product:</p>
    <ol>
        <li>Find the product in the list</li>
        <li>Click the <i class="fas fa-edit"></i> edit icon</li>
        <li>Make your changes</li>
        <li>Click <strong>Update Product</strong></li>
    </ol>
