    <h3 id="account">Account Settings</h3>
    <p>To update your information:</p>
    <ol>
        <li>Go to your Profile page</li>
        <li>Edit your username or email</li>
        <li>Click "Update Profile" to save changes</li>
    </ol>
    
    <div class="help-note">
        <i class="fas fa-exclamation-triangle"></i> <strong>Note:</strong> Changing your email will require verification.
    </div>
    
    <h3 id="password">Changing Password</h3>
    <p>To update your password:</p>
    <ol>
        <li>Go to the Account tab in your Profile</li>
        <li>Enter your current password</li>
        <li>Enter and confirm your new password</li>
        <li>Click "Change Password"</li>
    </ol>
    
    <h3 id="wishlist">Managing Your Wishlist</h3>
    <p>Your wishlist allows you to:</p>
    <ul>
        <li><strong>View saved items</strong> - See all products you've saved</li>
        <li><strong>Remove items</strong> - Click "Remove" next to any item</li>
        <li><strong>View products</strong> - Click "View" to go to the product page</li>
    </ul>
    
    <h3 id="security">Security Features</h3>
    <p>Your profile includes:</p>
    <ul>
        <li>Login history</li>
        <li>Active sessions</li>
        <li>Password strength indicator</li>
    </ul>
