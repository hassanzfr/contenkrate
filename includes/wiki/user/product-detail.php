<div class="help-section">
    <h2 id="product-detail">Product Details Guide</h2>
    <p>Learn how to view and interact with product details.</p>
    
    <h3 id="viewing">Viewing Product Information</h3>
    <p>Each product page contains:</p>
    <ul>
        <li><strong>Image Gallery</strong> - Click to zoom and view different angles</li>
        <li><strong>Product Name</strong> - Title of the product</li>
        <li><strong>Price</strong> - Current price with any discounts applied</li>
        <li><strong>Description</strong> - Detailed information about the product</li>
        <li><strong>Specifications</strong> - Technical details and features</li>
        <li><strong>Available Options</strong> - Variants like size, color, etc.</li>
    </ul>
    
    <div class="help-note">
        <i class="fas fa-lightbulb"></i> <strong>Tip:</strong> Use the "View Options" button to see all available configurations.
    </div>
    
    <h3 id="wishlist">Using the Wishlist</h3>
    <p>To save products for later:</p>
    <ol>
        <li>Make sure you're logged in</li>
        <li>Navigate to the product you want to save</li>
        <li>Click the <i class="far fa-heart"></i> "Add to Wishlist" button</li>
        <li>View your saved items in your <a href="profile.php">Profile</a></li>
    </ol>
    
    <h3 id="purchase">Making a Purchase</h3>
    <p>When ready to buy:</p>
    <ol>
        <li>Select any desired options</li>
        <li>Click the <i class="fab fa-amazon"></i> "Buy on Amazon" button</li>
        <li>Complete checkout on Amazon's website</li>
    </ol>
</div>